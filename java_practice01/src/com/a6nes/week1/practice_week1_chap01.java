package com.a6nes.week1;

public class practice_week1_chap01 {
    public static void main(String[] args) {

        /* Application01 */

        int a = 20; int b = 30;
        System.out.println("더하기 결과 : " + (a + b));
        System.out.println("빼기 결과 : " + (a - b));
        System.out.println("곱하기 결과 : " + (a * b));
        System.out.println("나누기한 몫 : " + (a / b));
        System.out.println("나누기한 나머지 : " + (a % b));

        /* Application02 */

        double width = 12.5; double height = 36.4;
        double area = width * height;
        double circumference = 2 * (width + height);
        System.out.println("면적 : " + area);
        System.out.println("면적 : " + circumference);

    }

}
